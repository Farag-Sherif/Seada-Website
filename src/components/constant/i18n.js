/**
 * Robust translate for menu items that may be written as:
 *  "About-Us" / "about-us" / "about_us" / "About Us"
 *
 * Usage:
 *   import i18n, { trMenu } from "@/i18n";
 *   {trMenu(t, item.titleKey || item.title)}
 */
export const trMenu = (t, keyOrText) => {
    if (!keyOrText) return "";
    const variants = [
        keyOrText,
        keyOrText.toLowerCase(),
        keyOrText.replace(/-/g, "_"),
        keyOrText.toLowerCase().replace(/-/g, "_"),
        keyOrText.replace(/_/g, "-"),
    ];
    for (const v of variants) {
        const r = t(v);
        if (r && r !== v) return r;
    }
    return keyOrText; // fallback if no translation found
};

